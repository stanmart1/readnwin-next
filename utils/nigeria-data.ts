export const getNigerianStates = () => nigerianStates;

export const getStateByName = (stateName: string): NigerianState | undefined => {
  return nigerianStates.find(state => state.name === stateName);
};

export const getStateByCode = (stateCode: string): NigerianState | undefined => {
  return nigerianStates.find(state => state.code === stateCode);
};

export const getLGAsByState = (stateName: string): string[] => {
  const state = getStateByName(stateName);
  return state ? state.lgas : [];
};

export const getLGAsByStateCode = (stateCode: string): string[] => {
  const state = getStateByCode(stateCode);
  return state ? state.lgas : [];
}; 